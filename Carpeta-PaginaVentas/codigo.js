//Aquí te menciono los diferentes eventos de mouse que puedes utilizar con el método addEventListener() de JavaScript:

//click: se activa cuando se hace clic en el elemento.
//dblclick: se activa cuando se hace doble clic en el elemento.
//mousedown: se activa cuando se presiona el botón del mouse mientras el cursor está sobre el elemento.
//mouseup: se activa cuando se suelta el botón del mouse después de presionarlo mientras el cursor está sobre el elemento.
//mouseenter: se activa cuando el cursor entra en el área del elemento.
//mouseleave: se activa cuando el cursor sale del área del elemento.
//mousemove: se activa cuando el cursor se mueve dentro del área del elemento.
//contextmenu: se activa cuando se hace clic con el botón derecho del mouse en el elemento para abrir el menú contextual.
//Cada uno de estos eventos de mouse puede ser utilizado como primer parámetro del método addEventListener(), seguido de una función que se ejecutará cuando se active el evento.


